package com.campuslands.modules.manufacture.domain;

public class Manufacture {
    int id;
    String name;

    public Manufacture(){}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    
    
}
